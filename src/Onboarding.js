import { CommonActions } from "@react-navigation/native";
import React, { useEffect } from "react";
import {
  ImageBackground,
  StyleSheet,
  Dimensions
} from "react-native";

/////////////////////////////////////////////////////////////////////////////////////
// Constants
/////////////////////////////////////////////////////////////////////////////////////

const { height, width } = Dimensions.get("screen");

/////////////////////////////////////////////////////////////////////////////////////
// Components
/////////////////////////////////////////////////////////////////////////////////////

const Onboarding = ({ navigation }) => {
  useEffect(() => {
    setTimeout(() => {
      navigation.dispatch(
        CommonActions.reset({
          index: 1,
          routes: [{ name: 'Home' }],
        })
      )
    }, 1200)
  }, [])
  return (
    <ImageBackground
      source={require("../assets/loaded_splash.png")}
      style={styles.imageContainer}
    />
  );
}

/////////////////////////////////////////////////////////////////////////////////////
// Stylesheet
/////////////////////////////////////////////////////////////////////////////////////

const styles = StyleSheet.create({
  container: {
    backgroundColor: "white"
  },
  imageContainer: {
    height,
    width
  }
});

export default Onboarding;
